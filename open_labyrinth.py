# Go clockwise
ROUTINE = {"S": ("W", "S", "E", "N"),
           "W": ("N", "W", "S", "E"),
           "N": ("E", "N", "W", "S"),
           "E": ("S", "E", "N", "W")}
MOVE = {"S": (1, 0), "N": (-1, 0), "W": (0, -1), "E": (0, 1)}


def next_pos(current, add):
    return (current[0] + add[0], current[1] + add[1])


def checkio(maze_map):
    direction = "N"
    route = ""
    pos = (1, 1)
    goal = (10, 10)
    passed = [pos]
    while pos != goal:
        for d in ROUTINE[direction]:
            tmp_pos = next_pos(pos, MOVE[d])
            if maze_map[tmp_pos[0]][tmp_pos[1]] == 0:
                if tmp_pos in passed:
                    direction = ROUTINE[direction][-1]
                    route += d
                    passed.append(pos)
                    print("R:" + route)
                    print("passwd:" + str(passed))
                    break
                pos = tmp_pos
                route += d
                direction = d
                passed.append(pos)
                print("R:" + route)
                print("passwd:" + str(passed))
                break
        else:
            assert False, "loop detect."

    return route


