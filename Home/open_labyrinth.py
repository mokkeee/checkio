MOVE = {"S": (1, 0), "N": (-1, 0), "W": (0, -1), "E": (0, 1)}
DIRECTIONS = ["E", "S", "W", "N"]
OPPOSITE_DIRECTION = {"N":"S", "S":"N", "W":"E", "E":"W"}
GOAL = (10, 10)

def next_pos(current, add):
    return (current[0] + add[0], current[1] + add[1])

def routing(maze_map, pos, direction, route, passed):
    if direction is not None:
        directions_candidate = DIRECTIONS[:]
        directions_candidate.remove(OPPOSITE_DIRECTION[direction])
    else:
        directions_candidate = DIRECTIONS

    for d in directions_candidate:
        tmp_pos = next_pos(pos, MOVE[d])
        # enable route
        if maze_map[tmp_pos[0]][tmp_pos[1]] == 0:
            # GOAL!
            if tmp_pos == GOAL:
                print(route + d)
                return route + d

            # rope detect. route discard.
            if tmp_pos in passed:
                return None

            # add passed route
            tmp_passed = passed[:]
            tmp_passed.append(tmp_pos)

            # search route recursively
            r = routing(maze_map, tmp_pos, d, route + d, tmp_passed)
            if r is not None:
                return r
    # dead end.
    else:
        return None


def checkio(maze_map):
    pos = (1, 1)
    return routing(maze_map, pos, None, "", [])


